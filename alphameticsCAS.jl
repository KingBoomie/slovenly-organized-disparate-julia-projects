const CS = ConstraintSolver

# ╔═╡ fc760fa6-b9b3-4eac-bf7c-4ad3c356caaa
@bind description TextField(default="SEND + MORE = MONEY")

# ╔═╡ 3c149796-1639-4af1-b2b9-f49078db9906
begin
	m = Model(CS.Optimizer)
	parts = strip.(split(description, "="))
	str_result = parts[2]
	str_sums = strip.(split(parts[1], "+"))
	str_sums, str_result
end

# ╔═╡ 7d409d6c-b9cc-4ea4-9932-6d003e0abe0b


# ╔═╡ 25c5e105-ded0-40ca-89e0-85e70eb32a35
var_names = unique(vcat(collect(str_result), collect.(str_sums)...))

# ╔═╡ 4e211e3d-cec8-48ed-a5b1-bc198a8c0fc2
@variable(m, 0 <= vars[Symbol.(var_names)] <= 9, Int) |> Text

# ╔═╡ 8f2623ed-e4b4-4501-8cc4-54b76fe39496
begin
	start_vars = unique([[s[1] for s = str_sums]..., str_result[1]])
	for s in Symbol.(start_vars)
		@constraint(m, vars[s] >= 1)
	end
end

# ╔═╡ ab374d36-79ed-44dd-bb84-2c27bbe5a893
@constraint(m, vars in CS.AllDifferent())

# ╔═╡ f15f1a14-8495-45fb-bb67-dbc68bbbfe1a
function word_to_sum(word, vars, m)
	word_vars = [Symbol(c) for c=word]
	times = reverse(collect(take(iterated(x -> 10x, 1), length(word))))
	
	mults = map(zip(word_vars, times)) do (w, t)
		vars[w]*t
	end
	sum(mults)
end

# ╔═╡ 4f5498f2-70d4-4283-a307-7cb551b54abb
left = [word_to_sum(str, vars, m) for str in str_sums]

# ╔═╡ 807fc2d8-f472-4920-8380-a6abb1a654e6
right = word_to_sum(str_result, vars, m)

# ╔═╡ d36a8f20-97ee-4828-b09b-ee320fc11bee
@constraint(m, sum(left) == sum(right))

# ╔═╡ cfa9df6f-81d5-40a5-b029-4a83311e74d4
begin
	optimize!(m)
	var_values = [round(Int, value(vars[Symbol(name)])) for name = var_names]
	var_names_s = string.(var_names)
	replacements = Pair.(var_names, var_values)
	
	let result = description
	for sub = replacements
		result = replace(result, sub)
	end
		
	HTML("""<pre>
			$description    
			$result
			</pre>
			""")
	end
end

